package com.modificationAdresse.model;

public class Conseiller {
 private Long id;
 private String nom;
 private Canal canal;
 
public Long getId() {
	return id;
}
public void setId(Long id) {
	this.id = id;
}
public String getNom() {
	return nom;
}
public void setNom(String nom) {
	this.nom = nom;
}
public Canal getCanal() {
	return canal;
}
public void setCanal(Canal canal) {
	this.canal = canal;
}
 
 
}
